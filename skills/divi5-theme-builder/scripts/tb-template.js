'use strict';

/**
 * Divi 5 Theme Builder template helpers.
 *
 * Companion to divi5-page-generator/scripts/divi-builder.js: that builder emits
 * the block markup for module/section/row/column trees; these helpers add the
 * two things it lacks — dynamic-content ($variable) tokens, and the
 * `et_theme_builder` export envelope that wraps body markup into an importable
 * TB template with a `use_on` scope.
 *
 * See ../references/dynamic-content-bindings.md (binding syntax + module attr
 * paths) and ../references/programmatic-install.md (PHP install alternative).
 */

/**
 * A dynamic-content token for a post field, embedded as a plain string in a
 * module's content value (e.g. inside `content.innerContent.desktop.value`).
 *
 *   dynamicContent('_airloop_dogs_name')
 *   dynamicContent('_airloop_dogs_breed', { before: 'Breed: ' })
 *   dynamicContent('post_featured_image')        // Divi built-in field name
 *
 * @param {string} name  Field id — a raw post-meta key, or a Divi built-in
 *                        (post_title, post_featured_image, post_content, …).
 * @param {{before?: string, after?: string}} [settings]
 * @returns {string} `$variable({...})$`
 */
function dynamicContent(name, settings = {}) {
  if (typeof name !== 'string' || name === '') {
    throw new TypeError('dynamicContent(name): name must be a non-empty string');
  }
  const before = settings.before || '';
  const after = settings.after || '';
  return `$variable(${JSON.stringify({
    type: 'content',
    value: { name, settings: { before, after } },
  })})$`;
}

/**
 * A `use_on` scope string.
 *   useOn.postType('airloop_record')                 → every record detail page
 *   useOn.taxonomyTerm('airloop_connection', 60)      → records in term 60 only
 *
 * Taxonomy-term scoping also enables live dynamic-content preview in the VB —
 * see references/dynamic-content-bindings.md.
 */
const useOn = {
  postType: (postType) => `singular:post_type:${postType}:all`,
  taxonomyTerm: (taxonomy, termId) =>
    `singular:taxonomy:${taxonomy}:term:id:${termId}`,
};

/**
 * Wrap body-layout block markup into an importable `et_theme_builder` export
 * object — the exact JSON shape Divi's own Theme Builder → Export produces, so
 * it imports cleanly via Theme Builder → Import. Body-only by design (header /
 * footer left at 0/enabled): the Theme Builder is global shared state, so never
 * overwrite a site's header/footer.
 *
 * @param {object} opts
 * @param {string} opts.bodyMarkup   Divi block markup (from divi-builder.js,
 *                                    typically `placeholder([...])`).
 * @param {string} opts.useOn        A scope string (use the `useOn` helpers).
 * @param {string} [opts.title]      Template title shown in the TB UI.
 * @param {number} [opts.layoutId]   Internal layout id used to cross-reference
 *                                   template→layout in the export (any stable
 *                                   positive int unique within the file).
 * @returns {object} Importable `{context:'et_theme_builder', templates, layouts, …}`.
 */
function themeBuilderTemplate(opts) {
  const {
    bodyMarkup,
    useOn: useOnCondition,
    title = 'Generated Detail Template',
    layoutId = 1001,
  } = opts || {};

  if (typeof bodyMarkup !== 'string' || bodyMarkup === '') {
    throw new TypeError('themeBuilderTemplate: bodyMarkup must be a non-empty string');
  }
  if (typeof useOnCondition !== 'string' || useOnCondition === '') {
    throw new TypeError('themeBuilderTemplate: useOn must be a non-empty scope string');
  }

  return {
    context: 'et_theme_builder',
    templates: [
      {
        title,
        autogenerated_title: false,
        default: false,
        enabled: true,
        use_on: [useOnCondition],
        exclude_from: [],
        layouts: {
          header: { id: 0, enabled: true },
          body: { id: layoutId, enabled: true },
          footer: { id: 0, enabled: true },
        },
        description: '',
      },
    ],
    layouts: {
      [layoutId]: {
        context: 'et_builder',
        data: { [layoutId]: bodyMarkup },
        images: [],
        post_title: `${title} Body Layout`,
        post_type: 'et_body_layout',
        theme_builder: { is_global: false },
        global_colors: [],
        canvases: { local: [], global: [] },
        post_meta: [
          { key: '_et_pb_use_builder', value: 'on' },
          { key: '_et_pb_use_divi_5', value: 'on' },
        ],
      },
    },
    presets: {},
    global_colors: [],
    global_variables: [],
    has_default_template: false,
    has_global_layouts: false,
  };
}

module.exports = { dynamicContent, useOn, themeBuilderTemplate };

// ── Runnable self-check: `node tb-template.js` ──────────────────────────────
if (require.main === module) {
  const assert = require('assert');

  // dynamicContent emits a valid token whose payload round-trips as JSON.
  const tok = dynamicContent('_airloop_dogs_breed', { before: 'Breed: ' });
  assert.ok(tok.startsWith('$variable(') && tok.endsWith(')$'), 'token delimiters');
  const payload = JSON.parse(tok.slice('$variable('.length, -2)); // strip trailing ')$'
  assert.strictEqual(payload.value.name, '_airloop_dogs_breed');
  assert.strictEqual(payload.value.settings.before, 'Breed: ');

  assert.throws(() => dynamicContent(''), TypeError, 'empty name rejected');

  // useOn scope strings.
  assert.strictEqual(useOn.postType('airloop_record'), 'singular:post_type:airloop_record:all');
  assert.strictEqual(useOn.taxonomyTerm('airloop_connection', 60), 'singular:taxonomy:airloop_connection:term:id:60');

  // Envelope carries the scope, the body markup, and the builder meta.
  const env = themeBuilderTemplate({
    bodyMarkup: `<!-- wp:divi/placeholder --><!-- /wp:divi/placeholder -->`,
    useOn: useOn.taxonomyTerm('airloop_connection', 60),
    title: 'Dogs Detail',
    layoutId: 2001,
  });
  assert.strictEqual(env.context, 'et_theme_builder');
  assert.deepStrictEqual(env.templates[0].use_on, ['singular:taxonomy:airloop_connection:term:id:60']);
  assert.strictEqual(env.templates[0].layouts.body.id, 2001);
  assert.ok(env.layouts[2001].data[2001].includes('wp:divi/placeholder'));
  assert.strictEqual(env.layouts[2001].post_type, 'et_body_layout');
  assert.throws(() => themeBuilderTemplate({ useOn: 'x' }), TypeError, 'missing bodyMarkup rejected');

  console.log('tb-template.js self-check: all assertions passed');
}
